function getSavedLanguage() {
  const urlParams = new URLSearchParams(window.location.search);
  const urlLang = urlParams.get("lang");
  if (urlLang && translations[urlLang]) {
    localStorage.setItem("app_lang", urlLang);
    return urlLang;
  }
  const stored = localStorage.getItem("app_lang");
  if (stored && translations[stored]) {
    return stored;
  }
  return "tr";
}

const langLocaleMap = { tr: "tr-TR", en: "en-GB", de: "de-DE", fr: "fr-FR", es: "es-ES", it: "it-IT", ru: "ru-RU", ar: "ar-SA" };

function t(key, replacements) {
  const lang = getSavedLanguage();
  let text = (translations[lang] && translations[lang][key]) || (translations.tr && translations.tr[key]) || key;
  if (replacements) {
    Object.keys(replacements).forEach(function(k) {
      text = text.replace("{" + k + "}", replacements[k]);
    });
  }
  return text;
}

function setLanguage(lang) {
  if (!translations[lang]) return;

  localStorage.setItem("app_lang", lang);

  const dict = translations[lang];
  const meta = langMeta[lang];

  // Update HTML Dir & Lang attributes
  document.documentElement.lang = lang;
  document.documentElement.dir = meta.dir;

  if (meta.dir === "rtl") {
    document.body.classList.add("rtl");
  } else {
    document.body.classList.remove("rtl");
  }

  // Update UI Elements with data-i18n attributes
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  // Update placeholder attributes with data-i18n-placeholder
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) {
      el.placeholder = dict[key];
    }
  });

  // Update dropdown button label
  const flagEl = document.getElementById("currentLangFlag");
  const codeEl = document.getElementById("currentLangCode");
  if (flagEl) flagEl.textContent = meta.flag;
  if (codeEl) codeEl.textContent = meta.code;

  // Close dropdown if open
  const menu = document.getElementById("langMenu");
  if (menu) menu.classList.remove("show");

  // Update page title via data-i18n-title attribute on <html>
  const titleKey = document.documentElement.getAttribute("data-i18n-title");
  if (titleKey && dict[titleKey]) {
    document.title = dict[titleKey];
  }

  // Update date elements with data-i18n-date
  document.querySelectorAll("[data-i18n-date]").forEach(function(el) {
    const isoDate = el.getAttribute("data-i18n-date");
    if (isoDate) {
      const d = new Date(isoDate);
      el.textContent = d.toLocaleDateString(langLocaleMap[lang] || "tr-TR");
    }
  });

  // Update datetime elements with data-i18n-datetime
  document.querySelectorAll("[data-i18n-datetime]").forEach(function(el) {
    const isoDate = el.getAttribute("data-i18n-datetime");
    if (isoDate) {
      const d = new Date(isoDate);
      el.textContent = d.toLocaleString(langLocaleMap[lang] || "tr-TR");
    }
  });
}

function toggleLangMenu(e) {
  if (e) e.stopPropagation();
  const menu = document.getElementById("langMenu");
  if (menu) menu.classList.toggle("show");
}

// Global click to close menu
document.addEventListener("click", (e) => {
  const menu = document.getElementById("langMenu");
  const btn = document.getElementById("langDropdownBtn");
  if (menu && btn && !btn.contains(e.target) && !menu.contains(e.target)) {
    menu.classList.remove("show");
  }
});

// Auto initialize on DOM ready
document.addEventListener("DOMContentLoaded", () => {
  const currentLang = getSavedLanguage();
  setLanguage(currentLang);

  const btn = document.getElementById("langDropdownBtn");
  if (btn) {
    btn.addEventListener("click", toggleLangMenu);
  }
});
